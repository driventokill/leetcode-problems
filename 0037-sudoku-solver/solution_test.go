package sudokusolver

import (
	"reflect"
	"testing"
)

func Test_solveSudoku(t *testing.T) {
	tests := []struct {
		name     string
		board    [][]byte
		expected [][]byte
	}{
		{
			name: "标准数独测试",
			board: [][]byte{
				{'5', '3', '.', '.', '7', '.', '.', '.', '.'},
				{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
				{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
				{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
				{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
				{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
				{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
				{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
				{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
			},
			expected: [][]byte{
				{'5', '3', '4', '6', '7', '8', '9', '1', '2'},
				{'6', '7', '2', '1', '9', '5', '3', '4', '8'},
				{'1', '9', '8', '3', '4', '2', '5', '6', '7'},
				{'8', '5', '9', '7', '6', '1', '4', '2', '3'},
				{'4', '2', '6', '8', '5', '3', '7', '9', '1'},
				{'7', '1', '3', '9', '2', '4', '8', '5', '6'},
				{'9', '6', '1', '5', '3', '7', '2', '8', '4'},
				{'2', '8', '7', '4', '1', '9', '6', '3', '5'},
				{'3', '4', '5', '2', '8', '6', '1', '7', '9'},
			},
		},
		{
			name: "空数独测试",
			board: [][]byte{
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
			},
			expected: [][]byte{
				{'1', '2', '3', '4', '5', '6', '7', '8', '9'},
				{'4', '5', '6', '7', '8', '9', '1', '2', '3'},
				{'7', '8', '9', '1', '2', '3', '4', '5', '6'},
				{'2', '3', '1', '5', '6', '4', '8', '9', '7'},
				{'5', '6', '4', '8', '9', '7', '2', '3', '1'},
				{'8', '9', '7', '2', '3', '1', '5', '6', '4'},
				{'3', '1', '2', '6', '4', '5', '9', '7', '8'},
				{'6', '4', '5', '9', '7', '8', '3', '1', '2'},
				{'9', '7', '8', '3', '1', '2', '6', '4', '5'},
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			// 创建输入数据的副本
			input := make([][]byte, len(tt.board))
			for i := range tt.board {
				input[i] = make([]byte, len(tt.board[i]))
				copy(input[i], tt.board[i])
			}

			solveSudoku(input)

			if !reflect.DeepEqual(input, tt.expected) {
				t.Errorf("solveSudoku() = %v, want %v", input, tt.expected)
			}
		})
	}
}
